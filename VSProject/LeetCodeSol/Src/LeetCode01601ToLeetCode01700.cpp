#include "LeetCode01601ToLeetCode01700.hpp"

int LeetCode01601ToLeetCode01700::getMaximumGenerated(int n)
{
    if (n < 2)
    {
        return n;
    }
    else
    {
        int* p = new int[n + 1];
        p[0] = 0;
        p[1] = 1;

        int max = p[1];

        for (int i = 2; i < n + 1; ++ i)
        {
            if (i % 2)
            {
                p[i] = p[i / 2] + p[(i / 2) + 1];
            }
            else
            {
                p[i] = p[i / 2];
            }

            if (max < p[i])
            {
                max = p[i];
            }
        }

        delete[] p;
        return max;
    }
}


