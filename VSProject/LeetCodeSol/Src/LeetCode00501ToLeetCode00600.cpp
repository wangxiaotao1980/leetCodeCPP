int LeetCode00509::fib(int N)
{
    int data[2] = { 0, 1 };

    if (N > 1)
    {
        for (int i = 2; i <= N; ++i)
        {
            data[i % 2] += data[(i + 1) % 2];
        }
    }

    return data[N % 2];
}


